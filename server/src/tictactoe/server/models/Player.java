package tictactoe.server.models;

import java.sql.SQLException;
import tictactoe.server.db.DatabaseManger;

/**
 *
 * @author asoliman
 */
public class Player {
    
    private int id;
    private String firstName;
    private String lastName;
    private String email;
    private String img;
    private String password;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    @Override
    public int hashCode() {
        int hash = 5;
        hash = 97 * hash + this.id;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Player other = (Player) obj;
        if (this.id != other.id) {
            return false;
        }
        return true;
    }
    
    // Checking if the user is signedIn
    public boolean isSignedIn() throws ClassNotFoundException, SQLException {
        DatabaseManger checkSignedIn = new DatabaseManger();
        return checkSignedIn.isOnline();
    }    
}
